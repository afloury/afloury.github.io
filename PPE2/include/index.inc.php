					<p class="blanc">La musique assistée par ordinateur (MAO)
						regroupe l'ensemble des utilisations de l'informatique comme outil
						associé à la chaîne de création musicale depuis la composition
						musicale jusqu'à la diffusion des œuvres, en passant par la
						formation pédagogique au solfège ou aux instruments.</p>
					<img src="images/Logic.jpg" alt="" class="alignleft" />
					<p class="blanc2">La MAO fait son apparition auprès d'un public
						élargi dans les années 1970, lorsque les premières idées de
						synthétiseur associées à l'ordinateur voient le jour (Synclavier
						et Fairlight). Elle se démocratise dans les années 1980 grâce à la
						généralisation de la micro-informatique (ex. Commodore 64, Apple
						II et surtout Atari ST, premier ordinateur personnel, avec le CX5M
						de Yamaha, à intégrer une interface MIDI). Aujourd'hui tous les
						ordinateurs sont livrés avec une carte son et permettent donc
						potentiellement de composer, traiter, modifier le son ; des
						interfaces évoluées sont apparues (USB, Firewire, MLAN (en)…) qui
						permettent de communiquer avec tout type d'instruments ou
						appareils audio et de les piloter. L'augmentation constante de la
						puissance des ordinateurs favorise l'arrivée de logiciels offrant
						toujours plus de fonctionnalités, et dans le même temps
						apparaissent des logiciels libres ou gratuits qui répondent aux
						besoins les plus courants. Internet participe également au
						développement de la MAO par l'intermédiaire, par exemple, des
						sites qui proposent des formations musicales à distance, mettent à
						disposition des échantillons sonores ou diffusent sous forme
						électronique des partitions de musique tombée dans le domaine
						public, ainsi que grâce à tous les forums spécialisés sur la
						musique.</p>
